# You can find these measures on the web (try wikipedia), but just in case:
# • a barleycorn is a (very old) English measure of length. 1 meter is equal to 117.647
# barleycorns
# • a mile is equal to 1760 yards
# • a furlong is measure of distance, 220 yards
# • a fortnight is a measure of time, 2 weeks
# • the mach number is a measure of speed, the percentage of the speed of sound. Mach 1 is a
# speed equal to the speed of sound in air, which is 1130 feet/second. Mach 1.5 would be
# 1.5 times the speed of sound.
# • PSL is a speed, the percentage of the speed of light in a vacuum. The speed of light is
# 299,792,458 meters/second.